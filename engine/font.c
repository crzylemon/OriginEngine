/* Atlas: 16 chars per row, 6 rows = 96 chars. 128x48 pixels. */
#define ATLAS_COLS 16
#define ATLAS_ROWS 6
#define ATLAS_W (ATLAS_COLS * 8)
#define ATLAS_H (ATLAS_ROWS * 8)

void font_get_atlas(unsigned char** pixels, int* width, int* height) {
    static unsigned char atlas[ATLAS_W * ATLAS_H * 4];
    memset(atlas, 0, sizeof(atlas));

    for (int ch = 0; ch < 95; ch++) {
        int col = ch % ATLAS_COLS;
        int row = ch / ATLAS_COLS;
        for (int y = 0; y < 8; y++) {
            unsigned char bits = g_font_data[ch][y];
            for (int x = 0; x < 8; x++) {
                int px = col * 8 + x;
                int py = row * 8 + y;
                int idx = (py * ATLAS_W + px) * 4;
                unsigned char on = (bits >> (7 - x)) & 1;
                atlas[idx + 0] = 255;
                atlas[idx + 1] = 255;
                atlas[idx + 2] = 255;
                atlas[idx + 3] = on ? 255 : 0;
            }
        }
    }

    *pixels = atlas;
    *width = ATLAS_W;
    *height = ATLAS_H;
}

int font_build_text(TextVertex* verts, const char* text,
                    float x, float y, float scale,
                    float r, float g, float b) {
    int count = 0;
    float cx = x;
    float char_w = scale;
    float char_h = scale * 2.0f;
    float uv_w = 1.0f / ATLAS_COLS;
    float uv_h = 1.0f / ATLAS_ROWS;

    for (int i = 0; text[i]; i++) {
        int ch = text[i] - 32;
        if (ch < 0 || ch >= 95) { cx += char_w; continue; }

        float u = (float)(ch % ATLAS_COLS) * uv_w;
        float v = (float)(ch / ATLAS_COLS) * uv_h;

        float sx = cx * 2.0f - 1.0f;
        float sy = y * 2.0f - 1.0f;
        float sw = char_w * 2.0f;
        float sh = char_h * 2.0f;

        verts[count++] = (TextVertex){{sx,    sy},     {u,        v},        {r,g,b}};
        verts[count++] = (TextVertex){{sx+sw, sy},     {u + uv_w, v},        {r,g,b}};
        verts[count++] = (TextVertex){{sx+sw, sy+sh},  {u + uv_w, v + uv_h}, {r,g,b}};
        verts[count++] = (TextVertex){{sx,    sy},     {u,        v},        {r,g,b}};
        verts[count++] = (TextVertex){{sx+sw, sy+sh},  {u + uv_w, v + uv_h}, {r,g,b}};
        verts[count++] = (TextVertex){{sx,    sy+sh},  {u,        v + uv_h}, {r,g,b}};

        cx += char_w;
    }
    return count;
}

int font_build_text_centered(TextVertex* verts, const char* text,
                             float center_x, float y, float scale,
                             float r, float g, float b) {
    int len = (int)strlen(text);
    float total_w = len * scale;
    float x = center_x - total_w * 0.5f;
    return font_build_text(verts, text, x, y, scale, r, g, b);
}

int font_text_hit_test(const char* text, float x, float y, float scale,
                       float mouse_x, float mouse_y) {
    int len = (int)strlen(text);
    float w = len * scale;
    float h = scale * 2.0f;
    return (mouse_x >= x && mouse_x <= x + w &&
            mouse_y >= y && mouse_y <= y + h);
}
